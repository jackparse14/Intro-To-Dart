void main(List<String> args) {
  // print("Hello" + 2);
  var myAge = 20;
  print(myAge = 30);
  print(myAge);
  print(myAge ~/= 2); // compound assignment operator
  print(myAge);

  print(myAge *= 2);
  print(myAge += 2);
  print(myAge -= 2);
  print(myAge &= 12612);
  print(myAge |= 10);
  print(myAge ^= 2);
}
